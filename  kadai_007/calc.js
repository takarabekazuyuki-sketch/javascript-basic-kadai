// 変数numに1以上の正の整数を代入します [1], [2]
let num = 15;

// 条件分岐を使用して倍数を判定します
// 3と5の両方の倍数である場合を最初に判定する必要があります [1]
if (num % 3 === 0 && num % 5 === 0) {
    console.log("3と5の倍数です");
} 
// 変数numが3の倍数の場合 [1]
else if (num % 3 === 0) {
    console.log("3の倍数です");
} 
// 変数numが5の倍数の場合 [1]
else if (num % 5 === 0) {
    console.log("5の倍数です");
} 
// それ以外の場合、変数numの値を出力します [1]
else {
    console.log(num);
}